#!/bin/zsh

case "$(cat /etc/os-release 2>/dev/null)" in
  *"archlinux"* )
    cat <<-EOF

     [1;36m      /\\
     [1;36m     /  \\
     [1;36m    /\\   \\
     [1;34m   /      \\
     [1;34m  /   ,,   \\
     [1;34m /   |  |  -\\
     [1;34m/_-''    ''-_\\[0m

EOF
    ;;
  *"alpine"* )
    if [[ -d /ish ]]
      cat <<-EOF
[1m
           .:'     
        _ :'_      
     .'' '-' ''.   
    :        .-'   
    :       :      
     :       '-.   
      '._.-._.'[0m

EOF
    then
    elif [[ -f /.dockerenv ]] \
    || (( $(cat /proc/1/sched | head -n 1 | grep -oP '(?<=\()\d+') != 1))
    then
      cat <<-EOF
 [1m
 [34m           ::           
 [34m     ## :: ##      [36m_    
 [34m     :: ## ::     [36m( \__
 [34m  :: ## :: ## ::[36m_.'   _)
 [36m:"""""""""""""""   .'' 
 [36m:                 ;     
  [36m:              ;'      
   '[36m-.________.-'        
 [0m
EOF
    else
      cat <<-EOF

    [1;34m   /\\ /\\
      /[0;1m/ [34m\\  \\
     /[0;1m/   [34m\\  \\
    /[0;1m//    [34m\\  \\
    [0;1m//      [34m\\  \\
              \\[0m

EOF
    fi
    ;;
  "" ) ;;
  * )
  cat <<-EOF

     [34m     ___
         |[0m.. [34m|
         ([1;33m<> [0;34m)
        / [0;1m__[0;34m  \\
       ( [0;1m|## [0;34m) )
     [33;1m\\^^\\[0;1m|##[0;34m(/[33;1m^^/
      \\_/[0;34m____[33;1m\\_/[0m
    
EOF
    ;;
esac

echo "Welcome to [34;1m${HOST}[0m, [1m${USER}[0m.";
echo;

